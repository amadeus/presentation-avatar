export function getAvatarSpecs(size: $Values<typeof Sizes>): SizeSpec {
  if (process.env.PROJECT_ENV === 'development' && SizeSpecs[size] == null) {
    throw new Error(`getAvatarSpecs(): invalid size ${size}`);
  }
  return SizeSpecs[size];
}

export function getAvatarSize(size: $Values<typeof Sizes>): number {
  return getAvatarSpecs(size).size;
}

export function getStatusSize(size: $Values<typeof Sizes>): number {
  return getAvatarSpecs(size).status;
}

export function getStrokeSize(size: $Values<typeof Sizes>): number {
  return getAvatarSpecs(size).stroke;
}

export function getStatusMaskSize(size: $Values<typeof Sizes>): number {
  const {status, stroke} = getAvatarSpecs(size);
  return status + stroke;
}

export function getStatusOffset(size: $Values<typeof Sizes>): number {
  return getAvatarSpecs(size).offset;
}

